package com.axonect.aee.template.baseapp.application.config;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.InterceptorRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

@Configuration
public class HttpRequestMvcConfig implements WebMvcConfigurer {

    @Autowired
    private RequestUuidInterceptor requestUUIDInterceptor;

    @Override
    public void addInterceptors(InterceptorRegistry registry) {
        registry.addInterceptor(requestUUIDInterceptor);
    }

}
